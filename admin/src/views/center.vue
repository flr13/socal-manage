        <el-form-item :style='{"margin":"0 0 20px 0"}' v-if="flag=='xueshengxinxi'"  label="性别" prop="xingbie">
          <el-select v-model="ruleForm.xingbie"  placeholder="请选择性别">
            <el-option
                v-for="(item,index) in xueshengxinxixingbieOptions"
                v-bind:key="index"
                :label="item"
